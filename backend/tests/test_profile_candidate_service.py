def test_candidate_description_prefix_constant():
    """常量必须以方括号开头并以单空格结尾，确保 ``startswith`` 检查稳定。"""
    assert CANDIDATE_DESCRIPTION_PREFIX.startswith("[")
    assert CANDIDATE_DESCRIPTION_PREFIX.endswith(" ")
    assert re.match(r"^\[CANDIDATE\] $", CANDIDATE_DESCRIPTION_PREFIX)
