def _scalar(value):
    r = MagicMock()
    r.scalar_one_or_none.return_value = value
    return r


def _scalars_all(values):
    r = MagicMock()
    scalars = MagicMock()
    scalars.all.return_value = list(values)
    r.scalars.return_value = scalars
    return r


def _make_envelope(
    *,
    name: str = "auto-generated-pdf-3p",
    description: str | None = "Automatically generated by Universal Parser",
    priority: int = 0,
    heading_rules: list[dict] | None = None,
    boilerplate: dict | None = None,
    chunking: dict | None = None,
    tables: dict | None = None,
    match_rules: dict | None = None,
    evidence: dict | None = None,
    status: str = "pending_approval",
) -> dict:
    """构造一个与 ``UniversalParser.suggest_profile`` 同形态的候选 envelope。"""
    return {
        "profile": {
            "name": name,
            "description": description,
            "priority": priority,
            "enabled": False,
            "match_rules": match_rules
            or {"filename_regex": [], "content_regex": [], "min_content_match_count": 1},
            "heading_rules": heading_rules or [],
            "boilerplate": boilerplate
            or {
                "detection_mode": "both",
                "statistical_threshold": 0.5,
                "manual_patterns": [],
            },
            "tables": tables
            or {
                "cross_page_merge": True,
                "row_level_chunking": False,
                "collapse_merged_cells": "describe",
            },
            "chunking": chunking
            or {
                "min_tokens": 256,
                "max_tokens": 800,
                "overlap_tokens": 80,
                "respect_heading_level": 1,
                "protect_patterns": [],
            },
            "domain_dictionary_id": None,
        },
        "metadata": {
            "status": status,
            "source": "universal_parser",
            "evidence": evidence
            or {
                "page_count": 3,
                "heading_count": 2,
                "table_count": 0,
                "boilerplate_candidates": 0,
                "avg_block_chars": 42.0,
            },
        },
    }


