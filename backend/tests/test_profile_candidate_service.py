class TestSaveCandidateNameCollision:
    """``save_candidate`` 在名字冲突时自动追加 ``-N`` 后缀。"""

    @pytest.mark.asyncio
    async def test_collision_appends_suffix(self, mock_db):
        # 第一次查询：name 冲突；第二次查询：name-1 可用
        existing = DocumentProfile(
            name="auto-generated-pdf-3p",
            description=None,
            priority=0,
            enabled=True,
            match_rules={},
            heading_rules=[],
            boilerplate={},
            tables={},
            chunking={},
            version=1,
        )
        existing.id = uuid.uuid4()
        mock_db.execute = AsyncMock(
            side_effect=[
                _scalar(existing),  # name conflict
                _scalar(None),  # name-1 available
            ]
        )

        envelope = _make_envelope(name="auto-generated-pdf-3p")
        profile = await save_candidate(mock_db, envelope)
        assert profile.name == "auto-generated-pdf-3p-1"

    @pytest.mark.asyncio
    async def test_multiple_collisions_increment(self, mock_db):
        existing = DocumentProfile(
            name="placeholder",
            description=None,
            priority=0,
            enabled=True,
            match_rules={},
            heading_rules=[],
            boilerplate={},
            tables={},
            chunking={},
            version=1,
        )
        existing.id = uuid.uuid4()
        mock_db.execute = AsyncMock(
            side_effect=[
                _scalar(existing),  # base name
                _scalar(existing),  # -1
                _scalar(existing),  # -2
                _scalar(None),  # -3 available
            ]
        )
        envelope = _make_envelope(name="auto-generated-pdf-3p")
        profile = await save_candidate(mock_db, envelope)
        assert profile.name == "auto-generated-pdf-3p-3"


