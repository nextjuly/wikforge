class TestSaveCandidateHappyPath:
    """``save_candidate`` 正常路径。"""

    @pytest.mark.asyncio
    async def test_persists_candidate_with_disabled_and_prefix(self, mock_db):
        # 唯一性查询返回 None
        mock_db.execute = AsyncMock(return_value=_scalar(None))

        envelope = _make_envelope()
        profile = await save_candidate(mock_db, envelope)

        assert profile.enabled is False
        assert profile.description.startswith(CANDIDATE_DESCRIPTION_PREFIX)
        # 元数据 sentinel 写入了 match_rules
        meta = profile.match_rules.get(CANDIDATE_METADATA_KEY)
        assert meta is not None
        assert meta["status"] == "pending_approval"
        assert meta["source"] == "universal_parser"
        assert meta["evidence"]["page_count"] == 3
        # 原始 match_rules 字段仍然存在
        assert profile.match_rules["filename_regex"] == []
        assert profile.match_rules["min_content_match_count"] == 1

        mock_db.add.assert_called_once()
        added = mock_db.add.call_args.args[0]
        assert isinstance(added, DocumentProfile)
        assert added is profile

    @pytest.mark.asyncio
    async def test_get_candidate_metadata_round_trip(self, mock_db):
        mock_db.execute = AsyncMock(return_value=_scalar(None))
        envelope = _make_envelope()
        profile = await save_candidate(mock_db, envelope)

        meta = get_candidate_metadata(profile)
        assert meta is not None
        assert meta["status"] == "pending_approval"
        assert meta["evidence"]["page_count"] == 3

        # 非候选 Profile（手动构造 enabled=True）
        normal = DocumentProfile(
            name="normal",
            description="usual profile",
            priority=0,
            enabled=True,
            match_rules={},
            heading_rules=[],
            boilerplate={},
            tables={},
            chunking={},
            version=1,
        )
        assert get_candidate_metadata(normal) is None
        assert is_candidate_profile(normal) is False

    @pytest.mark.asyncio
    async def test_existing_prefix_in_description_not_doubled(self, mock_db):
        mock_db.execute = AsyncMock(return_value=_scalar(None))
        envelope = _make_envelope(
            description=f"{CANDIDATE_DESCRIPTION_PREFIX}LLM 推断",
        )
        profile = await save_candidate(mock_db, envelope)
        assert profile.description.count(CANDIDATE_DESCRIPTION_PREFIX) == 1


