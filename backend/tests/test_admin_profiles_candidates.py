# ─── POST /candidates ────────────────────────────────────────────────


class TestCreateCandidate:
    def test_create_returns_201_and_marks_disabled(self, client, mock_db):
        # 唯一性查询（save_candidate 内部）→ 没冲突
        mock_db.execute = AsyncMock(return_value=_scalar(None))

        response = client.post(
            "/api/admin/profiles/candidates",
            json=_make_envelope(),
        )
        assert response.status_code == 201, response.text
        body = response.json()

        # ── envelope 形态 ──
        assert set(body.keys()) == {"profile", "metadata"}
        prof = body["profile"]
        assert prof["enabled"] is False
        assert prof["description"].startswith(CANDIDATE_DESCRIPTION_PREFIX)
        assert prof["name"] == "auto-generated-pdf-2p"

        meta = body["metadata"]
        assert meta["status"] == "pending_approval"
        assert meta["source"] == "universal_parser"
        assert meta["evidence"]["page_count"] == 2

    def test_create_with_name_collision_appends_suffix(self, client, mock_db):
        existing = _build_candidate_orm(name="auto-generated-pdf-2p")
        mock_db.execute = AsyncMock(
            side_effect=[
                _scalar(existing),  # base 冲突
                _scalar(None),  # -1 可用
            ]
        )

        response = client.post(
            "/api/admin/profiles/candidates",
            json=_make_envelope(name="auto-generated-pdf-2p"),
        )
        assert response.status_code == 201
        assert response.json()["profile"]["name"] == "auto-generated-pdf-2p-1"

    def test_create_with_invalid_status_returns_400(self, client, mock_db):
        # 形态合法（pydantic 通过），但服务层校验 status
        mock_db.execute = AsyncMock(return_value=_scalar(None))
        response = client.post(
            "/api/admin/profiles/candidates",
            json=_make_envelope(status="approved"),
        )
        assert response.status_code == 400
        assert "status" in response.text

    def test_create_missing_metadata_returns_422(self, client, mock_db):
        # Pydantic 层就拒绝
        bad = _make_envelope()
        bad.pop("metadata")
        response = client.post("/api/admin/profiles/candidates", json=bad)
        assert response.status_code == 422

    def test_create_missing_profile_name_returns_422(self, client, mock_db):
        bad = _make_envelope()
        bad["profile"]["name"] = ""
        response = client.post("/api/admin/profiles/candidates", json=bad)
        assert response.status_code == 422


# ─── GET /candidates ─────────────────────────────────────────────────


class TestListCandidates:
    def test_list_returns_only_candidates(self, client, mock_db):
        candidate = _build_candidate_orm()
        # list_candidates 内部连续 2 次 execute
        mock_db.execute = AsyncMock(
            side_effect=[_scalars_all([candidate]), _scalars_all([candidate])]
        )

        response = client.get("/api/admin/profiles/candidates")
        assert response.status_code == 200, response.text
        body = response.json()
        assert body["total"] == 1
        assert len(body["candidates"]) == 1
        item = body["candidates"][0]
        assert item["profile"]["enabled"] is False
        assert item["profile"]["description"].startswith(CANDIDATE_DESCRIPTION_PREFIX)
        assert item["metadata"]["status"] == "pending_approval"
        assert item["metadata"]["evidence"]["page_count"] == 2

    def test_list_empty(self, client, mock_db):
        mock_db.execute = AsyncMock(
            side_effect=[_scalars_all([]), _scalars_all([])]
        )
        response = client.get("/api/admin/profiles/candidates")
        assert response.status_code == 200
        assert response.json() == {"candidates": [], "total": 0}


