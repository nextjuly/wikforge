        response = client.post(
            f"/api/admin/profiles/candidates/{candidate.id}/approve",
            json={"priority": 7, "enabled": False},
        )
        assert response.status_code == 200
        body = response.json()
        assert body["priority"] == 7
        assert body["enabled"] is False  # 显式 disable 仍允许（管理员意愿优先）

