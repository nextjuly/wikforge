# ─── POST /candidates/{id}/reject ────────────────────────────────────


class TestRejectCandidate:
    def test_reject_returns_204(self, client, mock_db):
        candidate = _build_candidate_orm()
        mock_db.execute = AsyncMock(return_value=_scalar(candidate))

        response = client.post(
            f"/api/admin/profiles/candidates/{candidate.id}/reject"
        )
        assert response.status_code == 204
        mock_db.delete.assert_awaited_once_with(candidate)

    def test_reject_then_get_returns_404(self, client, mock_db):
        # 第一次 POST reject：找到候选，删除
        candidate = _build_candidate_orm()

        # 后续 fetch_by_id 返回 None
        mock_db.execute = AsyncMock(
            side_effect=[
                _scalar(candidate),  # reject 内部 fetch
                _scalar(None),  # 第二次 reject 找不到
            ]
        )

        first = client.post(
            f"/api/admin/profiles/candidates/{candidate.id}/reject"
        )
        assert first.status_code == 204

        second = client.post(
            f"/api/admin/profiles/candidates/{candidate.id}/reject"
        )
        assert second.status_code == 404

    def test_reject_non_candidate_returns_404(self, client, mock_db):
        not_candidate = DocumentProfile(
            name="real",
            description="not candidate",
            priority=0,
            enabled=True,
            match_rules={},
            heading_rules=[],
            boilerplate={},
            tables={},
            chunking={},
            version=1,
        )
        not_candidate.id = uuid.uuid4()
        mock_db.execute = AsyncMock(return_value=_scalar(not_candidate))

        response = client.post(
            f"/api/admin/profiles/candidates/{not_candidate.id}/reject"
        )
        assert response.status_code == 404
