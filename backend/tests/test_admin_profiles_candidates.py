# ─── helpers ─────────────────────────────────────────────────────────


def _scalar(value):
    r = MagicMock()
    r.scalar_one_or_none.return_value = value
    return r


def _scalars_all(values):
    r = MagicMock()
    scalars = MagicMock()
    scalars.all.return_value = list(values)
    r.scalars.return_value = scalars
    return r


def _make_envelope(
    *,
    name: str = "auto-generated-pdf-2p",
    description: str | None = "Automatically generated by Universal Parser",
    status: str = "pending_approval",
    evidence: dict | None = None,
) -> dict:
    return {
        "profile": {
            "name": name,
            "description": description,
            "priority": 0,
            "enabled": False,
            "match_rules": {
                "filename_regex": [],
                "content_regex": [],
                "min_content_match_count": 1,
            },
            "heading_rules": [
                {"pattern": r"^[一二三四五六七八九十]+、", "level": 1, "strip_pattern": False},
            ],
            "boilerplate": {
                "detection_mode": "both",
                "statistical_threshold": 0.5,
                "manual_patterns": [],
            },
            "tables": {
                "cross_page_merge": True,
                "row_level_chunking": False,
                "collapse_merged_cells": "describe",
            },
            "chunking": {
                "min_tokens": 256,
                "max_tokens": 800,
                "overlap_tokens": 80,
                "respect_heading_level": 1,
                "protect_patterns": [],
            },
            "domain_dictionary_id": None,
        },
        "metadata": {
            "status": status,
            "source": "universal_parser",
            "evidence": evidence
            or {
                "page_count": 2,
                "heading_count": 2,
                "table_count": 0,
                "boilerplate_candidates": 0,
                "avg_block_chars": 38.5,
            },
        },
    }


def _build_candidate_orm(
    *,
    name: str = "auto-generated-pdf-2p",
    version: int = 1,
    description: str = f"{CANDIDATE_DESCRIPTION_PREFIX}LLM 推断",
    profile_id: uuid.UUID | None = None,
) -> DocumentProfile:
    p = DocumentProfile(
        name=name,
        description=description,
        priority=0,
        enabled=False,
        match_rules={
            "filename_regex": [],
            "content_regex": [],
            "min_content_match_count": 1,
            CANDIDATE_METADATA_KEY: {
                "status": "pending_approval",
                "source": "universal_parser",
                "evidence": {
                    "page_count": 2,
                    "heading_count": 2,
                    "table_count": 0,
                    "boilerplate_candidates": 0,
                    "avg_block_chars": 38.5,
                },
            },
        },
        heading_rules=[],
        boilerplate={"detection_mode": "both", "statistical_threshold": 0.5, "manual_patterns": []},
        tables={"cross_page_merge": True, "row_level_chunking": False, "collapse_merged_cells": "describe"},
        chunking={"min_tokens": 256, "max_tokens": 800, "overlap_tokens": 80, "respect_heading_level": 1, "protect_patterns": []},
        version=version,
    )
    p.id = profile_id or uuid.uuid4()
    p.created_at = datetime(2024, 1, 1, tzinfo=timezone.utc)
    p.updated_at = p.created_at
    p.domain_dictionary_id = None
    return p


